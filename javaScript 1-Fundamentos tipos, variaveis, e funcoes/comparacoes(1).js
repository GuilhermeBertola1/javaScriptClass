// == (comparação implicita);

const numero = 5;
const texto = '5';

console.log(numero == texto);

// === (comparação explicita);

console.log(numero === texto);

// typeof é para descobrir qual é o tipo dos dados(string, number, bollean ...);

console.log(typeof numero);
console.log(typeof texto);

// ==  só compara o valor;
// === compara o valor e o tipo de dado;

// utilizamos mais agora os === são as boas praticas; (conversão explicita);//